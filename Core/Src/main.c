void delay_us (uint16_t us)
{
	__HAL_TIM_SET_COUNTER(&htim1,0);
	while (__HAL_TIM_GET_COUNTER(&htim1) < us);
}

void step(char Stepper_ID, long int Stepper_Steps, unsigned char Stepper_Dir)
{

	if (Stepper_ID == 'X'){
		for(int i = 0; i < Stepper_Steps; i++){
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_3, Stepper_Dir);
			HAL_Delay(1);
		}
	}
	else if (Stepper_ID == 'Y'){
		for(int i = 0; i < Stepper_Steps; i++){
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_8, Stepper_Dir);
			HAL_Delay(1);
		}
	}
	else if (Stepper_ID == 'Z'){
		for(int i = 0; i < Stepper_Steps; i++){
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6, Stepper_Dir);
			HAL_Delay(1);
		}
	}
	else if (Stepper_ID == 'E'){
		for(int i = 0; i < Stepper_Steps; i++){
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_4, Stepper_Dir);
			HAL_Delay(1);
		}
	}
	else {
		printf("Incorrect Stepper_ID");
	}
}

